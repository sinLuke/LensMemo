import Foundation

func sign(_ value: Float) -> Float {
    if value > 0 {
        return 1
    } else {
        return -1
    }
}

func sign(_ value: Double) -> Double {
    if value > 0 {
        return 1
    } else {
        return -1
    }
}

func sign(_ value: Int) -> Int {
    if value > 0 {
        return 1
    } else {
        return -1
    }
}
